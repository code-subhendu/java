class Escape_sequence
{
    public static void main(String args[])
    {
        System.out.print("Hi hallow super sallo\t");
        System.out.println("kahan se ho");
        System.out.print("Hi hallow super sallo\n iye");
        System.out.println("kahan se ho");
        System.out.print("Hi hallow super\" sallo");
        System.out.print("Hi hallow super\\ sallo");
    }
}
